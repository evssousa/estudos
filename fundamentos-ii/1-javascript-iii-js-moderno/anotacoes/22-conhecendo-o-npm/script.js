/** ANOTAÇÕES
 * npm, ou node package manager, é um gerenciador de pacotes do Node.js;
 * OBS: Node.js é uma ferramenta que permite executar o javascript fora do navegador;
 * Por que o npm existe?
 *      - O módulos que já vimos são muito úteis para organizar nossas aplicações, mas essa não é a sua única vantagem;
 *      - Com o npm programadores podem compartilhar seus módulos com outros programadores;
 *      - O npm é um conjunto de ferramentas que permite compartilhar os módulos na internet, baixar módulos    para nosso próprio projeto e gerenciar os módulos instalados no nosso projeto;
 *      - Isso tudo aliado ao poder da comunidade open-source permite aumentar a nossa produtividade (princípio DRY, Don't Repeat Yourself)
 * O npm também pode ser visto como 3 recursos que trabalham juntos:
 *      1º - uma ferramenta de linha de comando para executar o npm;
 *      2º - um repositório online de pacotes, onde ficam hospedados os módulos que podemos baixar;
 *      3º - o site https://npmjs.com onde podemos navegar pelos módulos existentes no repositório;
 */