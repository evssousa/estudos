let nome = 'Everson'

console.log(nome) // mostra no console do navegador
console.error(nome) // mostra no console com uma coloração de erro (em vermelho)
console.warn(nome) // mostra no console com uma colocaração de aviso (em amarelo)

window.alert(nome) // representa a aba do navegador, o alert mostra um popup no navegador
window.prompt('Insira seu nome:') // prompt aparece um input em um popup
window.confirm('Confirma essa opção?') // mostra um popup de confirmação, com botão de ok

// Não precisa obrigatoriamente usar o window, podendo utilizar somente o alert, prompt e/ou confirm.